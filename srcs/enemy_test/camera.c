#include "so_long.h"

void	init_camera(t_so_long *so_long)
{
	so_long->go[0] = 0;
	so_long->go[1] = 0;
	if (so_long->gw < 15)
		so_long->go[0] = (14 - so_long->gw) / 2 * BPX + 30;
	if (so_long->gh < 8)
		so_long->go[1] = (7 - so_long->gh) / 2 * BPX + 30;
}
